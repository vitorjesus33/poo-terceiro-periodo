    // Estruturas de controle

    // Estruturas de controle são utilizadas para controlar o fluxo de execução de um programa.
    // As estruturas de controle são divididas em três tipos:

    // Estruturas de controle condicionais
    // Estruturas de controle de repetição
    // Estruturas de controle de interrupção

    // Estruturas de controle condicionais
    // As estruturas de controle condicionais são utilizadas para controlar o fluxo de execução de um programa baseado em condições.
    // As estruturas de controle condicionais são divididas em três tipos:

    // if
    // if-else
    // switch

    // if
    // A estrutura de controle if é utilizada para executar um bloco de código caso uma condição seja verdadeira.
    // Exemplo:

    // if (condição) {
    //     // bloco de código
    // }

    // if-else
    // A estrutura de controle if-else é utilizada para executar um bloco de código caso uma condição seja verdadeira, e outro bloco de código caso a condição seja falsa.
    // Exemplo:

    // if (condição) {
    //     // bloco de código
    // } else {
    //     // bloco de código
    // }

    // switch
    // A estrutura de controle switch é utilizada para executar um bloco de código baseado no valor de uma variável.
    // Exemplo:

    // switch (variável) {
    //     case valor1:
    //         // bloco de código
    //         break;
    //     case valor2:
    //         // bloco de código
    //         break;
    //     default:
    //         // bloco de código
    //         break;
    // }

    // Estruturas de controle de repetição
    // As estruturas de controle de repetição são utilizadas para executar um bloco de código repetidamente.
    // As estruturas de controle de repetição são divididas em três tipos:

    // for
    // while
    // do-while

    // for
    // A estrutura de controle for é utilizada para executar um bloco de código um número específico de vezes.
    // Exemplo:

    // for (inicialização; condição; incremento) {
    //     // bloco de código
    // }

    // while
    // A estrutura de controle while é utilizada para executar um bloco de código enquanto uma condição for verdadeira.
    // Exemplo:

    // while (condição) {
    //     // bloco de código
    // }

    // do-while
    // A estrutura de controle do-while é utilizada para executar um bloco de código pelo menos uma vez, e repetidamente enquanto uma condição for verdadeira.
    // Exemplo:

    // do {
    //     // bloco de código
    // } while (condição);

    // Estruturas de controle de interrupção
    // As estruturas de controle de interrupção são utilizadas para interromper a execução de um bloco de código.
    // As estruturas de controle de interrupção são divididas em dois tipos:

    // break
    // continue

    // break
    // A estrutura de controle break é utilizada para interromper a execução de um bloco de código.
    // Exemplo:

    // for (int i = 0; i < 10; i++) {
    //     if (i == 5) {
    //         break;
    //     }
    //     System.out.println(i);
    // }

    // continue
    // A estrutura de controle continue é utilizada para interromper a execução de um bloco de código e continuar a execução do próximo ciclo.
    // Exemplo:

    // for (int i = 0; i < 10; i++) {
    //     if (i == 5) {
    //         continue;
    //     }
    //     System.out.println(i);
    // }

